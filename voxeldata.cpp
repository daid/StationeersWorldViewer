class Node
{
public:
    int density = 0;
    std::unique_ptr<std::array<Node, 8>> children;

    void read_from_file(gzFile gzf)
    {
        uint8_t flags;
        gzread(gzf, &flags, 1);
        if (flags & 1)
        {
            gzread(gzf, &flags, 1);
            density = flags;
            gzread(gzf, &flags, 1); // vtype
        }
        else
        {
            density = 0;
            children = std::make_unique<std::array<Node, 8>>();
            for (auto &child : *children)
            {
                child.read_from_file(gzf);
                density += child.density;
            }
            density /= 8;
        }
    }

    int get(Vector3i pos, int mask)
    {
        if (!children || !mask)
            return density;
        int n = 0;
        if (pos.x & mask) n += 1;
        if (pos.y & mask) n += 2;
        if (pos.z & mask) n += 4;
        return (*children)[n].get(pos, mask >> 1);
    }

    int max_depth(Vector3i pos, int mask, int current_depth)
    {
        if (!children || !mask)
            return current_depth;
        int n = 0;
        if (pos.x & mask) n += 1;
        if (pos.y & mask) n += 2;
        if (pos.z & mask) n += 4;
        return (*children)[n].max_depth(pos, mask >> 1, current_depth - 1);
    }
};

class VoxelData
{
public:
    void load(std::string path)
    {
        for (int n = 0; n < 64; n++)
        {
            std::string filename = path + "/Terrain" + std::to_string(n) + ".dat";
            auto gzf = gzopen(filename.c_str(), "rb");
            if (gzf)
            {
                printf("%s\n", filename.c_str());
                uint32_t max_depth;
                gzread(gzf, &max_depth, 4);
                root[n].read_from_file(gzf);
            }
        }
    }
    std::array<Node, 64> root;

    int get(Vector3i pos, int max_depth)
    {
        pos.x = std::clamp(pos.x, 0, 4095);
        pos.y = std::clamp(pos.y, 0, 4095);
        pos.z = std::clamp(pos.z, 0, 4095);
        int n = 0;
        if (pos.x & 0x400) n += 1;
        if (pos.y & 0x400) n += 2;
        if (pos.z & 0x400) n += 4;
        if (pos.x & 0x800) n += 8;
        if (pos.y & 0x800) n += 16;
        if (pos.z & 0x800) n += 32;
        int mask = 0x200 >> max_depth;
        pos.x >>= max_depth;
        pos.y >>= max_depth;
        pos.z >>= max_depth;
        return root[n].get(pos, mask);
    }

    int max_depth(Vector3i pos)
    {
        pos.x = std::clamp(pos.x, 0, 4095);
        pos.y = std::clamp(pos.y, 0, 4095);
        pos.z = std::clamp(pos.z, 0, 4095);
        int n = 0;
        if (pos.x & 0x400) n += 1;
        if (pos.y & 0x400) n += 2;
        if (pos.z & 0x400) n += 4;
        if (pos.x & 0x800) n += 8;
        if (pos.y & 0x800) n += 16;
        if (pos.z & 0x800) n += 32;
        int mask = 0x200;
        return root[n].max_depth(pos, mask, 10);
    }

    int min_max_depth(Vector3i pos, int size) {
        int min_value = 10;
        for(int x=-1;x<2;x++) for(int y=-1;y<2;y++) for(int z=-1;z<2;z++) {
            min_value = std::min(min_value, max_depth(pos + Vector3i(size*x,size*y,size*z)));
        }
        return min_value;
    }
};

class Mesh
{
public:
    std::vector<Vector3f> points;

    void saveSTL(std::string filename)
    {
        FILE *f = fopen(filename.c_str(), "wb");
        char header[80] = "WorldData";
        fwrite(header, 1, 80, f);
        uint32_t triangle_count = points.size() / 3;
        fwrite(&triangle_count, 1, 4, f);
        uint16_t flags = 0;
        for (unsigned int n = 0; n < triangle_count; n++)
        {
            Vector3f normal = (points[n * 3 + 2] - points[n * 3 + 0]).cross(points[n * 3 + 1] - points[n * 3 + 0]).normalized();
            fwrite(&normal, 3, 4, f);
            fwrite(&points[n * 3 + 0], 3, 4, f);
            fwrite(&points[n * 3 + 2], 3, 4, f);
            fwrite(&points[n * 3 + 1], 3, 4, f);
            fwrite(&flags, 1, 2, f);
        }
        fclose(f);
    }

    void saveCustom(std::string filename)
    {
        std::vector<Vector3f> positions;
        std::vector<uint16_t> indices;
        std::unordered_map<uint32_t, std::vector<uint16_t>> vertex_lookup;

        for(auto p : points)
        {
            int result = -1;
            auto hash = int(p.x) ^ (int(p.y) << 9) ^ (int(p.z) << 17);
            if (vertex_lookup.find(hash) != vertex_lookup.end()) {
                for(auto idx : vertex_lookup[hash]) {
                    if ((positions[idx] - p).length() < 0.01) {
                        result = idx;
                        break;
                    }
                }
            }
            if (result == -1) {
                indices.push_back(positions.size());
                vertex_lookup[hash].push_back(positions.size());
                positions.push_back({4096-p.x, p.y, p.z});
            } else {
                indices.push_back(result);
            }
        }
        // // Flip triangles
        // for(unsigned int n=0; n<indices.size(); n+=3) {
        //     std::swap(indices[n+1], indices[n+2]);
        // }

        FILE* f = fopen(filename.c_str(), "wb");
        uint32_t u32 = positions.size();
        fwrite(&u32, 1, 4, f);
        u32 = indices.size();
        fwrite(&u32, 1, 4, f);
        fwrite(positions.data(), sizeof(Vector3f), positions.size(), f);
        fwrite(indices.data(), sizeof(uint16_t), indices.size(), f);
        fclose(f);
    }
};

