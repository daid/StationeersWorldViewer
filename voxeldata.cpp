static inline float getOffset(int a, int b)
{
    auto delta = b - a;
    if (delta == 0)
        return 0.5f;
    return float(128 - a) / float(delta);
}

int main(int argc, char **argv)
{
    if (argc < 2) return 1;
    VoxelData data;
    std::string world = argv[1];
    data.load(argv[2]);

    Vector3f edgeVertex[12];
    for(int maxDepth = 6; maxDepth > 0; maxDepth -= 1)
    {
        int chunkSize = 64;
        int stepSize = 1 << maxDepth;
        int meshSize = chunkSize * stepSize;
        printf("%d %d\n", meshSize, stepSize);
        for (int sx = 0; sx < 4096; sx+=meshSize) for (int sy = 0; sy < 2048; sy+=meshSize) for (int sz = 0; sz < 4096; sz+=meshSize)
        {
            if (data.min_max_depth({sx, sy, sz}, meshSize) > maxDepth + 6)
                continue;
            Mesh mesh;
            for (int x = 0; x < chunkSize; x++) for (int y = 0; y < chunkSize; y++) for (int z = 0; z < chunkSize; z++)
            {
                int cube[8];
                int mask = 0;
                for (int n = 0; n < 8; n++)
                {
                    cube[n] = data.get(Vector3i{sx, sy, sz} + (Vector3i{x, y, z} + MarchingCubes::vertexOffset[n]) * stepSize, maxDepth);
                    if (cube[n] > 128)
                        mask |= 1 << n;
                }
                auto flags = MarchingCubes::CUBE_EDGE_FLAGS[mask];
                if (!flags)
                    continue;
                for (int n = 0; n < 12; n++)
                {
                    if (flags & (1 << n))
                    {
                        auto a = MarchingCubes::EDGE_CONNECTION[n].first;
                        auto b = MarchingCubes::EDGE_CONNECTION[n].second;
                        auto f0 = getOffset(cube[a], cube[b]);
                        auto f1 = 1.0f - f0;
                        edgeVertex[n] = Vector3f(x, y, z) + Vector3f(MarchingCubes::vertexOffset[a]) * f1 + Vector3f(MarchingCubes::vertexOffset[b]) * f0;
                        edgeVertex[n] *= float(stepSize);
                        edgeVertex[n] += Vector3f(sx, sy, sz);
                    }
                }
                for (int idx = 0; MarchingCubes::triangleTable[mask][idx] >= 0; idx += 3)
                {
                    for (int n = 0; n < 3; n++)
                    {
                        mesh.points.push_back(edgeVertex[MarchingCubes::triangleTable[mask][idx + n]]);
                    }
                }
            }
            printf("%d %d %d %ld\n", sx, sy, sz, mesh.points.size());
            if (mesh.points.size() > 0) {
                mesh.saveCustom("data/" + world + "/" + std::to_string(maxDepth) + "_" + std::to_string(sx) + "_" + std::to_string(sy) + "_" + std::to_string(sz) + ".mesh");
            }
        }
    }
    return 0;
}
